
#Você precisa passar valores entre parênteses quando a função foi definida com parâmetros
#as funções esperam receber um valor para converter, por isso (valor)
def dolar_para_euro(valor): 
    return valor * 0.85 # Taxa de conversão: 1 dólar = 0.85 euros

def real_para_dolar(valor):
    return valor * 0.19 # Taxa de conversão: 1 real = 0.19 dólares